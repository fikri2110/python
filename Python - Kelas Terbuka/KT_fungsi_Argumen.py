'''operator matematika pertambahan logic fungsi'''

def tambah(nilai1,nilai2):
     '''fungsi dari tambah'''
     hasil = nilai1 + nilai2
     print(f"{nilai1} + {nilai2} = {hasil} ")
tambah(10,20)

'''operator matematika perkalian  logic '''

def perkalian(nilai1,nilai2):
    '''fungsi dari perkalian'''
    hasil = nilai1 * nilai2
    print(f"{nilai1}*{nilai2}={hasil}")
perkalian(10,9)

def pembagian(nilai1,nilai2):
    '''fungsi dari pembagian'''
    hasil = nilai1 / nilai2
    print(f"{nilai1}*{nilai2}={hasil}")
pembagian(10000,10)